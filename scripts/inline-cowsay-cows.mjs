#!/usr/bin/env node
// Reads the vendored cow art files from cowsay-cows/ and inlines them as a
// generated JavaScript module at agent/lib/cowsay/cows.generated.ts.
//
// Why inline?
// - The cow files use the .cow extension, which eve's lib/ discovery treats as
//   "not a supported authored module" and errors on. We can't keep them under
//   agent/lib/ as data files. The generated .ts file is a valid authored module
//   that eve will happily bundle.
// - At runtime, the inlined Map means no fs.readFileSync and no path concerns
//   across dev/prod/Vercel.
//
// Source-of-truth for the cow art lives at project-root cowsay-cows/ (one file
// per cow, named <cow>.cow). Run via `prepare` after install and as a prefix in
// the Vercel `eve` buildCommand. Idempotent: skipped when the existing file
// matches the current cows/ hash.

import { createHash } from "node:crypto";
import { readFileSync, writeFileSync, mkdirSync, statSync } from "node:fs";
import { dirname, join, resolve } from "node:path";

const projectRoot = resolve(dirname(new URL(import.meta.url).pathname), "..");
const cowsDir = resolve(projectRoot, "cowsay-cows");
const target = resolve(projectRoot, "agent/lib/cowsay/cows.generated.ts");

try {
  statSync(cowsDir);
} catch (err) {
  if (err.code === "ENOENT") {
    console.log(
      `[inline-cowsay-cows] ${cowsDir} not found; skipping (run a fresh fetch or vendor cows from https://github.com/piuccio/cowsay first).`,
    );
    process.exit(0);
  }
  throw err;
}

// Concatenate all cow files in sorted order to make the hash deterministic.
import { readdirSync } from "node:fs";
const names = readdirSync(cowsDir)
  .filter((n) => n.endsWith(".cow"))
  .sort();

const lines = [`${names.length} cow file(s):`];
for (const name of names) {
  const content = readFileSync(join(cowsDir, name), "utf8");
  lines.push(`--- ${name} ---\n${content}`);
}
const digest = createHash("sha256").update(lines.join("\n")).digest("hex");
const cowsHash = digest.slice(0, 16);

// Skip if up to date.
try {
  const existing = readFileSync(target, "utf8");
  if (existing.includes(`cowsSha256: "${cowsHash}"`)) {
    console.log(
      `[inline-cowsay-cows] up to date (cowsSha256=${cowsHash}, ${names.length} cows)`,
    );
    process.exit(0);
  }
} catch {
  // file doesn't exist yet — proceed
}

// Build the generated module as a record of cow name -> raw art.
const record = {};
for (const name of names) {
  const stem = name.replace(/\.cow$/, "");
  const content = readFileSync(join(cowsDir, name), "utf8");
  // JSON.stringify the content with backtick escaping for safe template-literal
  // embedding.
  record[stem] = content;
}

mkdirSync(dirname(target), { recursive: true });
writeFileSync(
  target,
  `// AUTO-GENERATED by scripts/inline-cowsay-cows.mjs. Do not edit.
// Source: cowsay-cows/ (vendored from https://github.com/piuccio/cowsay @ master)
// cowCount: ${names.length}
// cowsSha256: "${cowsHash}"
export const COW_FILES: Record<string, string> = ${JSON.stringify(record, null, 2)};
`,
  "utf8",
);

console.log(
  `[inline-cowsay-cows] wrote ${target} (${names.length} cows, sha256=${cowsHash})`,
);
