#!/usr/bin/env node
// Bundles node_modules/@letta-ai/letta-code/letta.js into a single standalone
// Node script using `bun build`, then writes that bundled artifact as a base64
// string into agent/.generated/letta-cli-content.ts.
//
// Why bundle first?
// - Shipping the raw letta.js file is not enough: it still imports bare deps
//   like `ws`, which are missing in Vercel's eve runtime because
//   experimentalServices does not install externalized packages into
//   /var/task/node_modules.
// - eve/Nitro's authored-module bundler (Rolldown) does not honor Vite-style
//   `?raw` imports for package files.
//
// Run via `prepare` in package.json (after install for local dev) and as a
// prefix in the Vercel eve service's buildCommand. Idempotent: skipped when the
// generated file already matches the current standalone bundle hash.

import { execFileSync } from "node:child_process";
import {
  readFileSync,
  writeFileSync,
  mkdirSync,
  statSync,
  rmSync,
} from "node:fs";
import { dirname, join, resolve } from "node:path";
import { tmpdir } from "node:os";
import { createHash } from "node:crypto";

const projectRoot = resolve(dirname(new URL(import.meta.url).pathname), "..");
const source = resolve(
  projectRoot,
  "node_modules/@letta-ai/letta-code/letta.js",
);
const target = resolve(projectRoot, "agent/.generated/letta-cli-content.ts");

try {
  statSync(source);
} catch (err) {
  if (err.code === "ENOENT") {
    console.log(
      `[inline-letta-cli] source not found at ${source}; skipping (likely a clean checkout before \`bun install\`)`,
    );
    process.exit(0);
  }
  throw err;
}

const bundleDir = join(tmpdir(), `letta-cli-bundle-${process.pid}`);
const bundledPath = join(bundleDir, "letta-standalone.js");
mkdirSync(bundleDir, { recursive: true });

try {
  execFileSync(
    "bun",
    ["build", source, "--target", "node", "--outfile", bundledPath],
    { cwd: projectRoot, stdio: ["ignore", "pipe", "pipe"] },
  );
} catch (err) {
  const message =
    err instanceof Error && "stderr" in err
      ? String(err.stderr || err.message)
      : err instanceof Error
        ? err.message
        : String(err);
  throw new Error(`[inline-letta-cli] bun build failed: ${message}`);
}

let bundledContent = readFileSync(bundledPath, "utf8");

// The Letta CLI bundle eagerly wires its image resize helper to the sharp-based
// implementation via a top-level `var useMagick = false;`, which immediately
// pulls in sharp on startup. Our memory tool runs the CLI in headless mode and
// does not need image resizing at all, but if we leave this flag as-is, the
// CLI fails before init on Vercel because sharp's native runtime isn't present.
// Flipping the constant to true keeps the resize helper on the ImageMagick path
// instead, avoiding sharp initialization entirely for our use case.
const SHARP_SWITCH = "var useMagick = false;";
if (!bundledContent.includes(SHARP_SWITCH)) {
  throw new Error(
    `[inline-letta-cli] expected marker ${JSON.stringify(SHARP_SWITCH)} not found in bundled CLI`,
  );
}
bundledContent = bundledContent.replace(SHARP_SWITCH, "var useMagick = true;");

const bundleHash = createHash("sha256").update(bundledContent).digest("hex");
const encoded = Buffer.from(bundledContent, "utf8").toString("base64");

try {
  const existing = readFileSync(target, "utf8");
  if (existing.includes(`bundleSha256: "${bundleHash}"`)) {
    console.log(
      `[inline-letta-cli] up to date (sha256=${bundleHash.slice(0, 12)}…)`,
    );
    process.exit(0);
  }
} catch {
  // file doesn't exist yet — proceed
}

mkdirSync(dirname(target), { recursive: true });
writeFileSync(
  target,
  `// AUTO-GENERATED by scripts/inline-letta-cli.mjs. Do not edit.
// Source: node_modules/@letta-ai/letta-code/letta.js
// bundledWith: bun build --target node
// patched: useMagick=true (avoid eager sharp startup)
// bundleSha256: "${bundleHash}"
// bundleBytes: ${bundledContent.length}
export const LETTA_CLI_SOURCE_BASE64: string =
  ${JSON.stringify(encoded)};
`,
  "utf8",
);

rmSync(bundleDir, { force: true, recursive: true });

console.log(
  `[inline-letta-cli] wrote ${target} (${bundledContent.length} bytes → ${encoded.length} base64 chars, sha256=${bundleHash.slice(0, 12)}…)`,
);
